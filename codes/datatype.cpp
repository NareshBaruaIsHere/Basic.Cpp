#include <bits/stdc++.h>
using namespace std;

int main() {
    
    int age = 25;
    int mark = -200;
    char grade = 'A';
    bool isAdult = true;

    cout<< age << " " << mark << " " << grade << " " << isAdult << endl;
    cout << "size of int = " << sizeof(int) << endl;
    cout << "size of char = " << sizeof(char) << endl;
    cout << "size of bool = " << sizeof(bool) << endl;
    
    return 0;
}