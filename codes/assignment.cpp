#include <bits/stdc++.h>
using namespace std;

int main() {
    
    int a = 10;
    int b = 10;
    a += 5; // a = a + 5 => 15;
    cout << a << endl;
    a -= 5; // a = a - 5 => 5;
    cout << a << endl;
    a *= 5; // a = a * 5 => 50;
    cout << a << endl;
    b /= 5; // b = b / 5 => 2;
    cout << b << endl;
    
    return 0;
}